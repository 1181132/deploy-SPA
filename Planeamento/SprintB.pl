tempoDeViagem(TruckName, CurrCap, [Head1A, Head1B|Tail1], [Head2|Tail2], [Head3|Tail3], TravelTime):-
    calcularPesoMaximoCamiao(TruckName, TruckMaxWeight),
    calcularEnergiaConsumida(Head1A, Head1B, TruckMaxWeight, Head2, EnergyConsumed),
    EnergyLeft is CurrCap - EnergyConsumed,
    energiaMinima(TruckName, MinEnergy),
    EnergyLeft >= MinEnergy,
    calculaTempoViagem(Head1A, Head1B, TruckMaxWeight, Head2, ElapsedTime),
    tempoDeViagem(TruckName, EnergyLeft, [Head1B|Tail1], Tail2, Tail3, TempVar1),
    TempVar2 is TempVar1 + ElapsedTime,
    TravelTime is Head3 + TempVar2.

